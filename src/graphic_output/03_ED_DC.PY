# -*- coding: utf-8 -*-
import os
import zipfile
import re
import shutil

ruta_xlsm = r"C:\A_GS1_PROYECTOS\UribeTorresJames.xlsm"




















































































































































































































































































































































































































































































































# Convertir el archivo .xlsm a .zip
ruta_zip = ruta_xlsm.replace(".xlsm", ".zip")
os.rename(ruta_xlsm, ruta_zip)

# Extraer contenido
ruta_extraida = ruta_zip.replace(".zip", "_extraido")
with zipfile.ZipFile(ruta_zip, 'r') as zip_ref:
    zip_ref.extractall(ruta_extraida)

# Modificar sheet3.xml si existe
ruta_sheet3 = os.path.join(ruta_extraida, "xl", "worksheets", "sheet3.xml")
if os.path.exists(ruta_sheet3):
    with open(ruta_sheet3, "r", encoding="utf-8") as file:
        contenido = file.read()
    contenido_modificado = re.sub(r'(sheet|objects|scenarios)="1"', r'\1="0"', contenido)
    with open(ruta_sheet3, "w", encoding="utf-8") as file:
        file.write(contenido_modificado)
else:
    print("No se encontró sheet3.xml en la ruta esperada.")

# Reempaquetar contenido modificado
elementos_a_comprimir = ["_rels", "docProps", "xl", "[Content_Types].xml"]
ruta_nuevo_zip = ruta_xlsm.replace(".xlsm", "_nuevo.zip")
with zipfile.ZipFile(ruta_nuevo_zip, 'w', zipfile.ZIP_DEFLATED) as nuevo_zip:
    for elemento in elementos_a_comprimir:
        ruta_elemento = os.path.join(ruta_extraida, elemento)
        if os.path.isdir(ruta_elemento):
            for carpeta_raiz, subcarpetas, archivos in os.walk(ruta_elemento):
                for archivo in archivos:
                    ruta_completa = os.path.join(carpeta_raiz, archivo)
                    ruta_relativa = os.path.relpath(ruta_completa, ruta_extraida)
                    nuevo_zip.write(ruta_completa, ruta_relativa)
        elif os.path.isfile(ruta_elemento):
            ruta_relativa = os.path.relpath(ruta_elemento, ruta_extraida)
            nuevo_zip.write(ruta_elemento, ruta_relativa)

# Renombrar zip modificado a .xlsm
ruta_xlsm_modificado = ruta_xlsm.replace(".xlsm", "1.xlsm")
os.rename(ruta_nuevo_zip, ruta_xlsm_modificado)

# Eliminar carpeta extraída y archivo zip original
shutil.rmtree(ruta_extraida)
os.remove(ruta_zip)

print("-------------GS--------------------------------------------------------------------------: " + ruta_xlsm_modificado)
